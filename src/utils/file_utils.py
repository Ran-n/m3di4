from blake3 import blake3
import mmap
# ------------------------------------------------------------------------------


# ------------------------------------------------------------------------------
def calculate_hash(filename: str) -> str:
    with open(filename, 'rb') as f:
        with mmap.mmap(f.fileno(), 0, access=mmap.ACCESS_READ) as mm:
            return blake3(mm).hexdigest()
